package ReceiverDevices;

public class TV {
    public void turnOn() {
        System.out.println("tv is on");
    }
    public void turnOff() {
        System.out.println("tv is off");
    }

    public void mute() {
        System.out.println("tv is muted");
    }

    public void UnMute() {
        System.out.println("tv is un muted");
    }
}
